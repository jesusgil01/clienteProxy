    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        NewUserInfo user = new NewUserInfo();
        user.setApellido(txtApellidos.getText());
        user.setCurp(txtCurp.getText());
        user.setDireccion(txtDireccion.getText());
        user.setEmail(txtEmail.getText());
        user.setEstadocivil(cbxEstado_Civil.getSelectedItem().toString());
        user.setFechanaci(txtFecha.getText());
        user.setGenero(cbxGenero.getSelectedItem().toString());
        user.setId_tienda(Integer.parseInt(txtTiend.getText()));
        user.setNombre(txtNombre.getText());
        char[] arrayPass = txtContra.getPassword();
        user.setPassword(new String(arrayPass));
        user.setRfc(txtRfc.getText());
        user.setRol(cbxRol.getSelectedItem().toString());
        user.setSalario(Double.parseDouble(txtSalario.getText()));
        user.setTelefono(txtTelefono.getText());
        user.setUsername(txtUsuario.getText());

        List<Validator<NewUserInfo>> validators = new ArrayList<>();
        List<String> errors = new ArrayList<>();
        validators.add(new EdadValidator());
        validators.add(new EmailValidator());
        validators.add(new NombreValidator());
        validators.add(new PasswordValidator());
        CompositeValidator composite = new CompositeValidator(validators);

        errors = composite.validate(user);

        if (errors.isEmpty()) {
            try {
                if (reportGenerator.existeUsuario(user)) {
                    JOptionPane.showMessageDialog(this, "El usuario ya existe", "Error!", JOptionPane.WARNING_MESSAGE);
                } else {
                    if (reportGenerator.insertUsuario(user)) {
                        JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente");
                        this.dispose();
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {
            String lista = "";
            StringBuilder sb = new StringBuilder();
            for (String error : errors) {
                sb.append(error + "\n");
            }
            lista = sb.toString();
            JOptionPane.showMessageDialog(this, lista, "Advertencia!", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

