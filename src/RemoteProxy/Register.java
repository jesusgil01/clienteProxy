    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        double salario = Double.parseDouble(txtSalario.getText());
        char genero = cbxGenero.getSelectedItem().toString().charAt(0);
        char estado_civil = cbxEstado_Civil.getSelectedItem().toString().charAt(0);
        char rol = cbxRol.getSelectedItem().toString().charAt(0);
        try {
            //Implementa el validator villa bb buapo
            if (reportGenerator.addUser(txtNombre.getText(), txtApellidos.getText() , txtFecha.getText(), genero, txtCurp.getText(), txtRfc.getText(), estado_civil, txtTelefono.getText(), txtEmail.getText(), rol, txtUsuario.getText(), txtContra.getName(), salario, txtDireccion.getText())) {
                JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente");
                this.dispose();
            } else {
                //Validator
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

